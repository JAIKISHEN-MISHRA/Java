package Printing_output_variables_datatypes;

public class Q5_Swap {
    public static void main(String[] args) {
        int no1=5,no2=6,temp;
        temp=no1;
        no1=no2;
        no2=temp;
        System.out.println("The number 1 become "+no1+" and number 2 becomes "+no2);
    }
}
